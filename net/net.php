PHP 网络函数
PHP 网络函数不需要安装，它们是 PHP 核心的一部分。
函数名称 	描述
checkdnsrr 	给指定的主机（域名）或IP地址做DNS通信检查
closelog 	关闭系统日志链接
dns_get_record 	获取指定主机的DNS记录
fsockopen 	：一个网络连接或者一个Unix专有连接
gethostbyaddr 	获取指定的IP地址对应的主机名
gethostbyname 	返回主机名对应的IPv4地址。
gethostbynamel 	获取互联网主机名对应的IPv4地址列表
gethostname 	获取主机名
getmxrr 	获取互联网主机名对应的MX记录
getprotobyname 	获取与协议名称关联的协议编号
getprotobynumber 	获取与协议编号关联的协议名称
getservbyname 	获取互联网服务协议对应的端口
getservbyport 	获取与端口和协议相对应的Internet服务
header_register_callback 	调用一个header函数
header_remove 	删除之前设置的HTTP头
header 	发送原生HTTP头
headers_list 	返回已发送的HTTP响应头（或准备发送的）
headers_sent 	检测HTTP头是否已经发送
http_response_code 	获取/设置响应的HTTP状态码
inet_ntop 	将压缩的互联网地址转换为人类可读的表示形式
inet_pton 	将人类可读的IP地址转换为其打包的in_addr表示形式
ip2long 	将IPV4的同轴互联网协议转换成长整型数字
long2ip 	将长整型转化为同轴形式带点的互联网标准格式地址（IPV4）
openlog 	打开与系统记录器的连接
pfsockopen 	一个持久的网络连接或Unix专有连接。
setcookie 	发送Cookie
setrawcookie 	发送声明URL编码的cookie
syslog 	生成系统日志消息